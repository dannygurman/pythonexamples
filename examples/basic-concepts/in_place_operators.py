# In-place operators allow you to write code like 'x = x + 3' more concisely,
#  as 'x += 3'.
# The same thing is possible with other operators such as -, *, / and % as well.
x = 2
print(x)
# 2
x += 3
print(x)
# 5

x = 4
x *= 3
print(x)
# 12

# These operators can be used on types other than numbers, as well, such as strings.

x = "spam"
x += "eggs"
print(x)
# spameggs

# Python does not have ++.
x = "a"
x *= 3
print(x)
# aaa








